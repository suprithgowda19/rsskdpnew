$uri = urldecode(
    parse_url($_SERVER['REQUEST_URI'], PHP_URL_PATH) ?? ''
);

// Legacy templates include "public/" because production serves the project
// directory. The local PHP server already uses public as its document root.
if (strpos($uri, '/public/') === 0 && file_exists(__DIR__.$uri)) {
    header('Location: '.substr($uri, 7), true, 302);
    exit;
}

// This file allows us to emulate Apache's "mod_rewrite" functionality from the
// built-in PHP web server. This provides a convenient way to test a Laravel
// application without having installed a "real" web server software here.
if ($uri !== '/' && file_exists(__DIR__.'/public'.$uri)) {
    return false;
}

require_once __DIR__.'/public/index.php';
